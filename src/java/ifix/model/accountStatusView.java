public class accountStatusView {
    
    private String accountStatusInvoice;
    private int accountStatusQty;
    private double accountStatusUnitPrice;
    private double accountStatusIncome;
    private double accountStatusExpences;

    /**
     * @return the accountStatusInvoice
     */
    public String getAccountStatusInvoice() {
        return accountStatusInvoice;
    }

    /**
     * @param accountStatusInvoice the accountStatusInvoice to set
     */
    public void setAccountStatusInvoice(String accountStatusInvoice) {
        this.accountStatusInvoice = accountStatusInvoice;
    }

    /**
     * @return the accountStatusQty
     */
    public int getAccountStatusQty() {
        return accountStatusQty;
    }

    /**
     * @param accountStatusQty the accountStatusQty to set
     */
    public void setAccountStatusQty(int accountStatusQty) {
        this.accountStatusQty = accountStatusQty;
    }

    /**
     * @return the accountStatusUnitPrice
     */
    public double getAccountStatusUnitPrice() {
        return accountStatusUnitPrice;
    }

    /**
     * @param accountStatusUnitPrice the accountStatusUnitPrice to set
     */
    public void setAccountStatusUnitPrice(double accountStatusUnitPrice) {
        this.accountStatusUnitPrice = accountStatusUnitPrice;
    }

    /**
     * @return the accountStatusIncome
     */
    public double getAccountStatusIncome() {
        return accountStatusIncome;
    }

    /**
     * @param accountStatusIncome the accountStatusIncome to set
     */
    public void setAccountStatusIncome(double accountStatusIncome) {
        this.accountStatusIncome = accountStatusIncome;
    }

    /**
     * @return the accountStatusExpences
     */
    public double getAccountStatusExpences() {
        return accountStatusExpences;
    }

    /**
     * @param accountStatusExpences the accountStatusExpences to set
     */
    public void setAccountStatusExpences(double accountStatusExpences) {
        this.accountStatusExpences = accountStatusExpences;
    }
    
    
    
}
